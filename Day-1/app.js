// variable rules
// 1. Cannot be a reserved keyword
// 2. Should be meaningful
// 3. Cannot start with a number (1name)
// 4. Cannot contain a space or hyphen (-)
// 5. Are case-sensitive

// legal and illegal variable names
// Legal
// let firstName;
// let lastName;
// let firstName1;
// let firstName_1;
// let firstName$1;
// let firstName$;
// let firstName$_;
// let firstName$_1;
// let firstName$_2;
// let firstName$_3;

// Illegal
// let 1firstName;
// let first-name;
// let first name;


